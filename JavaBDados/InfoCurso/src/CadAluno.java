    private void btnEditarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarAlunoActionPerformed
        int row = jTableAlunos.getSelectedRow();
        String id = jTableAlunos.getValueAt(row, 0).toString();

        String nome = jNomeAluno.getText();
        String cpf = jCpfAluno.getText();
        String rg = jRgAluno.getText();
        String telefone = jTelAluno.getText();
        String turma = jTurmaAluno.getText();        
            
        try {
            Conexao con = new Conexao();            
            Statement st = con.conexao.createStatement();

            String sql = "UPDATE aluno SET "
            + "nome='" + nome + "',"
            + "cpf='" + cpf + "',"
            + "rg='" + rg + "',"
            + "telefone='" + telefone + "',"
            + "turma='" + turma + "'"
            + " WHERE id = " + id;

            if (st.executeUpdate(sql) == 1) {
                JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
                popularTabelaAluno();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao alterar");
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
        }
            

        
    }//GEN-LAST:event_btnEditarAlunoActionPerformed

    private void btnExcluirAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirAlunoActionPerformed
        int linha = jTableAlunos.getSelectedRow();
        String id = jTableAlunos.getValueAt(linha, 0).toString();

        int confirmacao = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir a linha?");

        if (confirmacao == 0) {
            try {
                Conexao con = new Conexao();
                Statement st = con.conexao.createStatement();
                String sqlDelete = "DELETE FROM aluno WHERE id = " + id;

                if (st.execute(sqlDelete)) {
                    JOptionPane.showMessageDialog(null, "Não foi possível excluir a linha!");
                } else {
                    JOptionPane.showMessageDialog(null, "Deletado com sucesso!");
                    limparAluno();
                    popularTabelaAluno();
                }

            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnExcluirAlunoActionPerformed

