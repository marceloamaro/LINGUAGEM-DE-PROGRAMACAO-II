public void popularTabelaPessoas() throws SQLException {
        String nomeBuscado = jTextNomePesquisa.getText();
        String tipoPessoa = jComboTipoPessoa.getSelectedItem().toString();

        DefaultTableModel model = (DefaultTableModel) tabelaPessoa.getModel();
        model.setNumRows(0);

        Conexao con = new Conexao();

        Statement st = con.conexao.createStatement();

        String sql = "SELECT * FROM pessoas";

        if (!nomeBuscado.isEmpty() && !tipoPessoa.equals("Tipo pessoa")) {
            sql += " WHERE nome LIKE '" + nomeBuscado + "%' AND serie = '" + tipoPessoa + "'";
        } else if (!tipoPessoa.equals("Tipo pessoa")) {
            sql += " WHERE serie = '" + tipoPessoa + "'";
        } else if (!nomeBuscado.isEmpty()) {
            sql += " WHERE nome LIKE '" + nomeBuscado + "%'";
        }

        ResultSet resultado = st.executeQuery(sql);

        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("id"),
                resultado.getString("nome"),
                resultado.getString("curso"),
                resultado.getString("serie"),
                resultado.getString("matricula"),});
        }

    }

public void popularTabelaLivros() throws SQLException {
        String nomeLivro = jTextNomePesquisa1.getText();
        String tipoLivro = jComboTipoLivro.getSelectedItem().toString();

        DefaultTableModel model = (DefaultTableModel) tabelaLivro.getModel();
        model.setNumRows(0);

        Conexao con = new Conexao();

        Statement st = con.conexao.createStatement();

        String sql = "SELECT * FROM livro";

        if (!nomeLivro.isEmpty() && !tipoLivro.equals("Todos")) {
            sql += " WHERE nome_livro LIKE '" + nomeLivro + "%' AND genero = '" + tipoLivro + "'";
        } else if (!tipoLivro.equals("Todos")) {
            sql += " WHERE genero = '" + tipoLivro + "'";
        } else if (!nomeLivro.isEmpty()) {
            sql += " WHERE nome_livro LIKE '" + nomeLivro + "%'";
        }

        ResultSet resultado = st.executeQuery(sql);

        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("id_livro"),
                resultado.getString("nome_livro"),
                resultado.getString("autor"),
                resultado.getString("ano"),
                resultado.getString("genero"),
                resultado.getString("edicao"),
                resultado.getString("estoque"),});
        }

    }

