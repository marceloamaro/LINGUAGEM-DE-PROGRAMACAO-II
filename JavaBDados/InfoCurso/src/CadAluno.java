    public void popularTabelaTurmas() throws SQLException{
        String buscaTurma = jTxtBuscaTurma.getText();
        String jComboTurma = jComboPesquisaTurma.getSelectedItem().toString();
        
        DefaultTableModel model = (DefaultTableModel) jTableTurma.getModel();
        model.setNumRows(0);
        
        Conexao con = new Conexao();
        Statement st = con.conexao.createStatement();
        
        String sql = "SELECT * FROM turma";
        
        if (!buscaTurma.isEmpty() && !jComboTurma.equals("Todos")) {
            sql += " WHERE primeira_aula LIKE '" + buscaTurma + "%' OR segunda_aula LIKE '" + buscaTurma + "%' AND monitor = '" + jComboTurma + "'";
        } else if (!jComboTurma.equals("Todos")) {
            sql += " WHERE monitor = '" + jComboTurma + "'";
        } else if (!buscaTurma.isEmpty()) {
            sql += " WHERE primeira_aula LIKE '" + buscaTurma + "%' OR segunda_aula LIKE '" + buscaTurma + "%'";
        }
        
        ResultSet resultado = st.executeQuery(sql);
        
        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("id"),
                resultado.getString("monitor"),
                resultado.getString("primeira_aula"),                
                resultado.getString("segunda_aula"),
                resultado.getString("horario"),});
        }
    }
    
    public void popularTabelaAluno() throws SQLException {
        String nomeBuscado = jBuscaNome.getText();
        String jComboAluno = jComboPesquisaAluno.getSelectedItem().toString();

        DefaultTableModel model = (DefaultTableModel) jTableAlunos.getModel();
        model.setNumRows(0);

        Conexao con = new Conexao();

        Statement st = con.conexao.createStatement();

        String sql = "SELECT * FROM aluno";

        if (!nomeBuscado.isEmpty() && !jComboAluno.equals("Todos")) {
            sql += " WHERE nome LIKE '" + nomeBuscado + "%' AND turma = '" + jComboAluno + "'";
        } else if (!jComboAluno.equals("Todos")) {
            sql += " WHERE turma = '" + jComboAluno + "'";
        } else if (!nomeBuscado.isEmpty()) {
            sql += " WHERE nome LIKE '" + nomeBuscado + "%'";
        }
        

        ResultSet resultado = st.executeQuery(sql);

        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("id"),
                resultado.getString("nome"),
                resultado.getString("cpf"),
                resultado.getString("rg"),
                resultado.getString("telefone"),
                resultado.getString("turma"),});
        }
                    
    }
    
