    private void btnCadastrarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarAlunoActionPerformed
        String nomeAluno = jNomeAluno.getText();
        String cpfAluno = jCpfAluno.getText();
        String rgAluno = jRgAluno.getText();
        String telefoneAluno = jTelAluno.getText();
        String turmaAluno = jTurmaAluno.getText();
        
        try {
            Conexao con = new Conexao();
            Statement st = con.conexao.createStatement();
            
            String sql = "INSERT INTO aluno (nome, cpf, rg, telefone, turma) VALUES ('" + nomeAluno + "', '" + cpfAluno + "', '" + rgAluno + "', '" + telefoneAluno + "', '" + turmaAluno + "')";
        
            if (st.execute(sql)) {
                    JOptionPane.showMessageDialog(null, "OPS! Tivemos um erro aqui.");
                } else {
                    JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso!");
                    limparAluno();
                    popularTabelaAluno();
                    
                }
            
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCadastrarAlunoActionPerformed

