    public void popularTabela() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tblPessoas.getModel();
        model.setNumRows(0);

        Conexao con = new Conexao();
        Statement st = con.conexao.createStatement();

        String nome = jTextPesquisar.getText();
        String serie = jComboSerie.getSelectedItem().toString();

        String sql = "SELECT * FROM pessoas";

        if (!nome.equals("") && !serie.equals("TIPO PESSOA")) {
            sql += " WHERE nome LIKE '" + nome + "%' AND serie = '" + serie + "'";
        } else if (!nome.equals("")) {
            sql += " WHERE nome LIKE '" + nome + "%'";
        } else if (!serie.equals("TIPO PESSOA")) {
            sql += " WHERE serie = '" + serie + "'";
        }

        System.out.println(sql);
        ResultSet resultado = st.executeQuery(sql);

        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("id"),
                resultado.getString("nome"),
                resultado.getString("email"),
                resultado.getString("serie")
            });
        }
    }

