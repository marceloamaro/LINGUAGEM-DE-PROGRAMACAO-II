    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int row = tblPessoas.getSelectedRow();
        String id = tblPessoas.getValueAt(row, 0).toString();
        try {
            int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir?");
            Conexao con = new Conexao();
            if (confirmacao == 0) {
                Statement st = con.conexao.createStatement();
                String sql = "DELETE FROM pessoas WHERE id = " + id;

                if (st.execute(sql)) {
                    JOptionPane.showMessageDialog(null, "ERRO AO SALVAR");
                } else {
                    JOptionPane.showMessageDialog(null, "ITEM DELETADO COM SUCESSO!");
                    jTextPesquisar.setText("");
                    jComboSerie.setSelectedIndex(0);
                    popularTabela();
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(ListarPessoas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

